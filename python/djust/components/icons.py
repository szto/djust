# Built-in sets
_BUILTIN_ICON_SETS = {
    "heroicons": HEROICONS_OUTLINE,
}

# Cache for merged (builtin + user) icon sets
_icon_sets_cache: Optional[Dict[str, Dict[str, str]]] = None


def _get_icon_sets() -> Dict[str, Dict[str, str]]:
    """Return the merged icon sets dict (builtin + user-defined).

    User sets are loaded from ``settings.DJUST_COMPONENTS_ICON_SETS`` on first
    access and cached thereafter.
    """
    global _icon_sets_cache
    if _icon_sets_cache is not None:
        return _icon_sets_cache

    merged = dict(_BUILTIN_ICON_SETS)
    try:
        from django.conf import settings

        user_sets = getattr(settings, "DJUST_COMPONENTS_ICON_SETS", None)
        if user_sets and isinstance(user_sets, dict):
            merged.update(user_sets)
    except Exception as exc:
        # Custom icon sets are optional; any settings/import error falls back to built-ins.
        logger.debug("Custom DJUST_COMPONENTS_ICON_SETS unavailable: %s", exc)

    _icon_sets_cache = merged
    return merged


def clear_icon_sets_cache() -> None:
    """Clear the icon sets cache (useful for testing)."""
    global _icon_sets_cache
    _icon_sets_cache = None


