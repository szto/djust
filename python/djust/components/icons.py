def render_icon(
    name: str,
    size: Union[str, int] = "md",
    icon_set: str = "heroicons",
    custom_class: str = "",
    **attrs: Any,
) -> str:
    """Render an SVG icon as a safe HTML string.

    This is the shared Python helper used by template tags, Rust handlers,
    and component classes.

    Args:
        name: Icon name (e.g. "check", "x-mark", "sun")
        size: Size preset — "xs" (12px), "sm" (16px), "md" (20px), "lg" (24px),
              or an integer for custom pixel size
        icon_set: Icon set name (default "heroicons")
        custom_class: Additional CSS class(es)
        **attrs: Extra HTML attributes (e.g. aria_label="Close")

    Returns:
        A ``mark_safe()`` HTML string, or empty string if icon not found.
    """
    sets = _get_icon_sets()
    icon_data = sets.get(icon_set, {})
    svg_inner = icon_data.get(name)
    if svg_inner is None:
        return ""

    # Resolve size
    if isinstance(size, str):
        px = ICON_SIZES.get(size, ICON_SIZES["md"])
    else:
        px = int(size)

    # Build CSS classes
    classes = ["dj-icon"]
    size_label = size if isinstance(size, str) and size in ICON_SIZES else None
    if size_label:
        classes.append(f"dj-icon--{size_label}")
    if custom_class:
        classes.append(_html.escape(custom_class))
    class_str = " ".join(classes)

    # Build extra attributes string
    extra = ""
    for key, val in attrs.items():
        # Convert python_name to html-name (aria_label -> aria-label)
        attr_name = _html.escape(key.replace("_", "-"))
        attr_val = _html.escape(str(val))
        extra += f' {attr_name}="{attr_val}"'

    svg: str = mark_safe(
        f'<svg class="{class_str}" xmlns="http://www.w3.org/2000/svg" '
        f'width="{px}" height="{px}" viewBox="0 0 24 24" '
        f'fill="none" stroke="currentColor" stroke-width="1.5" '
        f'aria-hidden="true"{extra}>'
        f"{svg_inner}"
        f"</svg>"
    )
    return svg


def get_icon_names(icon_set: str = "heroicons") -> List[str]:
    """Return a sorted list of available icon names in the given set."""
    sets = _get_icon_sets()
    return sorted(sets.get(icon_set, {}).keys())


def get_icon_sets() -> List[str]:
    """Return a list of available icon set names."""
    return sorted(_get_icon_sets().keys())
